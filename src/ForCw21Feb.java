
public class ForCw21Feb {

	public static void main(String[] args) {
		
		for(int i=1 ;i <=5; i++) {
			System.out.println("Print the value in For loop" + i);
	}
// Reverese the order 
		System.out.println("Reverse the Order follows below");
		for(int n=5 ;n >=1; n--) {
			System.out.println("Print the value in For loop" + n);
	}
		
		
}
}
